package com.kprit.tns.backendtraining.oopsdemo;

public class Encapsulation {
	private int sid;
	private String sname;
	
	public void setSid(int sid) {
		this.sid = sid;
	}
	public int getSid() {
		return sid;
	}
	
	public void setSname(String sname) {
		this.sname = sname;
	}
	public String getSname() {
		return sname;
	}

}
